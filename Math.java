class Math

{  public static void main ( String[] args )

{     // defining number for program
	int num = 50 ;
	int factor = 2 ;
	int sum = 0 ;
     // calculations
	sum = num + factor ;
	System.out.println ( "add these together " + sum );
	sum = num - factor ;
	System.out.println ( "minus these values " + sum );
	sum = num * factor ;
	System.out.println ( "times these together" + sum );
	sum = num / factor ;
	System.out.println ( "divide these values" + sum );
	
}





}